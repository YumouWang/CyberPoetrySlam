package model;

/**
 * The Position model class
 *
 * Created by Nathan on 10/3/2014.
 */
public class Position {
    int x;
    int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
